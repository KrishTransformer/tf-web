const calcReducer = (state = initialState, action) => {
  switch (action.type) {
    case constants.ADD_CALC:
      return {
        ...state,
        [action.calcName]: {
          ...state[action.calcName],
          isLoading: true,
          isFullfilled: false,
          isFailed: false,
        },
      };

    case constants.ADD_CALC_FULFILLED:
      return {
        ...state,
        [action.calcName]: {
          ...state[action.calcName],
          isLoading: false,
          isFullfilled: true,
          data: action.response, // Consistently use `response`
          metadata: action.metadata,
        },
      };

    case constants.ADD_CALC_FAILED:
      return {
        ...state,
        [action.calcName]: {
          ...state[action.calcName],
          isLoading: false,
          isFullfilled: false,
          isFailed: true,
        },
      };

    case constants.CLEAR_CALC_FULFILLED:
      return initialState;

    case constants.GENERATE_3D:
      return {
        ...state,
        [action.calcName]: {
          ...state[action.calcName],
          isLoading: true,
          isFullfilled: false,
          isFailed: false,
        },
      };
    case constants.LOAD_3D:
      return {
        ...state,
        [action.calcName]: {
          ...state[action.calcName],
          isLoading: true,
          isFullfilled: false,
          isFailed: false,
        },
      };
    case constants.GENERATE_3D_FULFILLED:
      return {
        ...state,
        [action.calcName]: {
          ...state[action.calcName],
          isLoading: false,
          isFullfilled: true,
          data: action.response, // Consistently use `response`
        },
      };

    case constants.GENERATE_3D_FAILED:
      return {
        ...state,
        [action.calcName]: {
          ...state[action.calcName],
          isLoading: false,
          isFullfilled: false,
          isFailed: true,
        },
      };

    case constants.GENERATE_3D_CLEARED:
      return {
        ...state,
        [action.calcName]: {
          ...state[action.calcName],
          isLoading: false,
          isFullfilled: false,
          isFailed: true,
          data: {}
        },
      };

    default:
      return state; // Return the current state as the default case
  }
};

export default calcReducer;
